    public static <T> T getOrElse(Option<T> option, Closure<T> closure) {
        return option.getOrElse(closure::call);
    }

    public static <T> Option<T> orElse(Option<T> option, Closure<Option<T>> closure) {
        return option.orElse(closure::call);
    }

    public static <T, L> Either<L, T> toEither(Value<T> value, Closure<L> closure) {
        return value.toEither(closure::call);
    }

    public static <T, R> Either<T, R> toLeft(Value<T> value, Closure<R> closure) {
        return value.toLeft(closure::call);
    }

    @SuppressWarnings("unchecked")
    public static <R, T extends Tuple> R getAt(T tuple, Integer index) {
        return (R) tuple.toSeq().get(index);
    }

    public static <T, X extends Throwable> T getOrElseThrow(
            Try<T> aTry,
            @ClosureParams(value = FromString.class, options = {"java.lang.Throwable"})
                    Closure<X> closure) throws X {
        return aTry.getOrElseThrow((Function<Throwable, X>) closure::call);
    }

    public static <L, R> Either<L, R> peek(
            Either<L, R> either,
            @ClosureParams(value = FirstParam.SecondGenericType.class) Closure closure) {
        return either.peek(closure::call);
    }

